	<h1>About</h1>

	<p>Without compromising on any individual rates or individual company identities, SeaDex uses secure and proven technology to connect Freight Forwarders with Consumers of container freight, worldwide.</p>

	<p>The challenge for the 'Freight Buyer' is that containerized shipping is a very complex industry and the pricing structure is diffucult to understand.</p>

	<p>Seadex users will benefit from a transparent view of the pricing, routes, schedules and ship ownership all which are variables and factors for you to evaluate the value proposition. 
Seadex matches your requirements either as a 'Freight Buyer' or 'Freight Seller' delivering the best 'Value" for the deal based on your requirements.
Selecting a 'Freight Supplier' is not just picking the cheapest price, the factors that Seadex provides includes key indicators including;</p>

	<ul>
	<li>Price</li>
	<li>Availability</li>
	<li>Transit Time</li>
	<li>Transit Route - Direct or Indirect</li>
	<li>Quality and Reputation	</li>
	</ul>
	
	<h2>SeaDex enables:</h2>
	
	<ul>
	<li>RFQ's only sent to the most competitive Freight Forwarders for the respective route</li>
	<li>Freight Forwarders compete for your shipments</li>
	<li>Significantly decrease time spent on sourcing vendors (ISO 9001)</li>
	<li>Contract Bidding from all Logistic Companies and Freight Forwarders (Launch Q4 - 2014)</li>
	<li>Benefit from other companies price negotiations</li>
	<li>Decrease your Carbon Footprint in LCL shipments (ISO 14001)</li>
	<li>Seadex Market allows reduced cost for LCL cargoes (Launch - Q3 2014)</li>
	</ul>
	
	<h2>History</h2>
	
	<p>SeaDex was founded by the six co-founders in 2013. The common ground between the founders was several experiences, both private and professional, dealing with the difficulty and time consumption in sourcing the optimal service providers when shipping goods in containers, overseas.</p>
	
